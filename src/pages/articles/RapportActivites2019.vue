            <p class="text-xl font-semibold text-[#090e15]">
              Un rapport complet sur les projets de sensibilisation et de renforcement des capacités menés dans la région des savanes
            </p>

            <h2 class="text-3xl font-bold text-[#090e15] mt-8 mb-4">Introduction</h2>
            <p>
              La Région des savanes est la région la plus septentrionale du Togo. Avec une population d'environ 824 000 habitants selon les résultats fournis par le 4ème RPGH de 2010, elle est la région la plus pauvre parmi les cinq (05) que compte le pays avec une incidence de pauvreté estimée à plus de 90%. Parmi les sept préfectures que compose cette région, celles de Kpendjal et Kpendjal-Ouest sont les plus pauvres avec un taux de 96,5%.
            </p>
            <p>
              Il faut noter que 90% de la population totale de la région est rurale. Les activités économiques de cette population sont basées essentiellement sur l'agriculture, l'élevage et les produits forestiers. Elles constituent les principales sources de leurs revenus. L'agriculture représente plus de 40% du PIB national et occupe près de 65% de sa population active.
            </p>
            <p>
              C'est face à ces énormes constats que l'association EDUCATEL PLUS et l'ONG MDH ont accentué leur action pour l'année 2019 dans la protection de l'environnement et l'agriculture durable.
            </p>

            <h2 class="text-3xl font-bold text-[#090e15] mt-8 mb-4">Projet 1 : Sensibilisation sur l'Utilisation des Pesticides</h2>

            <h3 class="text-2xl font-semibold text-[#090e15] mt-6 mb-3">Contexte</h3>
            <p>
              Afin de répondre à une demande croissante et atteindre des niveaux de productions économiques viables, les agriculteurs de la préfecture de Kpendjal Ouest utilisent les produits phytosanitaires contre les mauvaises herbes et les attaques parasitaires. Le glyphosate (herbicide) et les néonicotinoides (insecticides) se retrouvent en tête suivis des fongicides.
            </p>
            <p>
              Cependant face à l'utilisation anarchique de ces pesticides non homologués par les agriculteurs, l'association EDUCATEL PLUS et l'ONG Mobilisation pour le Développement Humain ont initié et exécuté ce projet à hauteur de 2 000 000 de francs CFA co-financé par les deux partenaires.
            </p>

            <h3 class="text-2xl font-semibold text-[#090e15] mt-6 mb-3">Objectifs</h3>
            <ul class="list-disc pl-6 space-y-2">
              <li>Contribuer à la protection de la biodiversité et à la gestion durable des terres cultivables</li>
              <li>Réduire le taux d'intoxication lié à l'utilisation des pesticides</li>
              <li>Lutter contre la dégradation des sols par l'utilisation des pesticides non homologués</li>
              <li>Réduire l'utilisation des pesticides</li>
            </ul>

            <h3 class="text-2xl font-semibold text-[#090e15] mt-6 mb-3">Sensibilisation des Agriculteurs</h3>
            <p>
              L'équipe technique a sillonné la Préfecture de Kpendjal Ouest pour rencontrer les acteurs et discuter des effets néfastes de l'utilisation abusive et incontrôlée des pesticides.
            </p>
            <p>
              <strong>Effets néfastes sur l'environnement :</strong> L'utilisation des pesticides comporte des inconvénients dont la pollution de l'environnement et les risques d'intoxication. Les pesticides polluent l'eau et l'air, détruisent la faune et modifient dangereusement le fonctionnement de l'écosystème.
            </p>
            <p>
              <strong>Effets néfastes sur les populations :</strong> Les utilisateurs sont exposés aux pesticides pendant les opérations de traitement. L'absence d'application des mesures d'hygiène expose dangereusement les populations aux maladies.
            </p>
            <p>
              <strong>Mode d'utilisation et bonnes pratiques :</strong> Les agriculteurs ont été sensibilisés sur les modes d'usage et les conditions pratiques d'utilisation incluant la maîtrise du dosage et la nécessité de se protéger lors du traitement avec des équipements appropriés.
            </p>

            <h2 class="text-3xl font-bold text-[#090e15] mt-8 mb-4">Projet 2 : Renforcement de Capacité des Pépiniéristes</h2>

            <h3 class="text-2xl font-semibold text-[#090e15] mt-6 mb-3">Contexte</h3>
            <p>
              En vue de permettre aux producteurs de plants de la Préfecture de Kpendjal Ouest de maîtriser les techniques de production de pépinière et de produire les plants en quantité et en qualité suffisante, EDUCATEL PLUS et l'ONG MDH ont initié un projet de renforcement des capacités avec un budget total de 3 000 000 de francs CFA.
            </p>

            <h3 class="text-2xl font-semibold text-[#090e15] mt-6 mb-3">Activités Réalisées</h3>
            <p>
              <strong>Identification des pépiniéristes :</strong> Une tournée de prise de contact avec les autorités locales a été menée. Les bénéficiaires ont été choisis sur la base des critères de professionnalisme, de motivation et de disponibilité. Les pépiniéristes ont été organisés en groupement dirigé par un comité composé de sept membres.
            </p>
            <p>
              <strong>Formation et équipement :</strong> Des ateliers de formation ont été organisés dans chaque canton bénéficiaire portant sur :
            </p>
            <ul class="list-disc pl-6 space-y-2">
              <li>Les généralités sur les pépinières</li>
              <li>La récolte-conservation des semences forestières</li>
              <li>La levée de dormance</li>
              <li>L'aménagement de l'espace de la pépinière</li>
              <li>La recherche de terreaux</li>
              <li>L'empotage, le semis et les entretiens</li>
            </ul>
            <p>
              Les formations ont été essentiellement pratiques, permettant de disposer au moins 500 pots dans chaque canton. Le matériel de pépinière distribué comprenait des brouettes, pelles, pioches, coupe-coupe, râteaux, pots, semences, et mètres à ruban.
            </p>

            <h3 class="text-2xl font-semibold text-[#090e15] mt-6 mb-3">Résultats Obtenus</h3>
            <ul class="list-disc pl-6 space-y-2">
              <li>30 pépiniéristes identifiés, formés et outillés en matériels de production de plants</li>
              <li>Amélioration des connaissances des producteurs sur la conduite de la pépinière</li>
              <li>Une pépinière installée dans chaque canton pour produire au moins 5000 plants</li>
              <li>Pépinières fonctionnelles à Nayega, Ogaro, Naki EST et Namoundjoga</li>
            </ul>

            <h3 class="text-2xl font-semibold text-[#090e15] mt-6 mb-3">Difficultés Rencontrées</h3>
            <ul class="list-disc pl-6 space-y-2">
              <li>Insuffisance de matériel roulant pour le travail du terrain</li>
              <li>Difficulté d'accès dans certains villages bénéficiaires due à la montée des eaux des rivières</li>
              <li>Perte de certains pieds de plants plantés au cours de la saison sèche</li>
            </ul>

            <h3 class="text-2xl font-semibold text-[#090e15] mt-6 mb-3">Perspectives d'Avenir</h3>
            <ul class="list-disc pl-6 space-y-2">
              <li>Renforcer le personnel</li>
              <li>Étendre le réseau de partenariat</li>
              <li>Mieux répondre aux besoins des populations rurales</li>
              <li>Étendre les champs d'action</li>
            </ul>

            <h2 class="text-3xl font-bold text-[#090e15] mt-8 mb-4">Conclusion Générale</h2>
            <p>
              Tous les résultats obtenus dans la mise en œuvre des différents projets de 2019 ont impacté positivement sur la vie des populations cibles et sur l'environnement. Ce qui atteste l'efficacité des actions de l'association EDUCATEL PLUS et l'ONG MDH sur le plan socio-économique et environnemental.
            </p>
